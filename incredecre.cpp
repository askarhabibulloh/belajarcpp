#include <iostream>
using namespace std;

int main(){
    
    //increment decrement
    int a=5,b=5;

    //pre increment
    cout<<a<<endl;
    cout<<++a<<endl;
    cout<<a<<endl<<endl;


    //post increment
    cout<<b<<endl;
    cout<<b++<<endl;
    cout<<b<<endl<<endl;


    //pre decrement
    cout<<a<<endl;
    cout<<--a<<endl;
    cout<<a<<endl<<endl;


    //post decrement
    cout<<b<<endl;
    cout<<b--<<endl;
    cout<<b<<endl;


    return 0;
}
